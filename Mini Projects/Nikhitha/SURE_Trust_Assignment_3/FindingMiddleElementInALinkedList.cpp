class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int getMiddle(Node *head)
    {
        Node* temp = head;
        int len = 0;
        while(temp!=NULL){
            len++;
            temp = temp->next;
        }
        if(len==1){
            return head->data;
        }
        
        Node* curr = head;
        int mid = len/2+1;
        int cnt = 1;
        while(curr!=NULL){
            if(cnt==mid){
                return curr->data;
            
            }
            cnt++;
            curr=curr->next;
        }
        return -1;
    }
};


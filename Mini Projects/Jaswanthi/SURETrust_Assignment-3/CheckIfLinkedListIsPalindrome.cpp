class Solution{
  public:
    bool isPalindrome(Node *head)
    {
      stack<char> stk;
        for (Node *curr = head; curr != NULL;curr=curr->next)
        {
            stk.push(curr->data);
        }
        for (Node *curr = head; curr != NULL;curr=curr->next)
        {
            if(stk.top()!=curr->data)
            {
                return false;
            }
            stk.pop();
        }
        return true;
    }
};

